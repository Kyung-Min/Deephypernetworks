function bundle = calculate_sift_histogram(bundle)
    K = 300; % sift cluster size    
    for i=1:size(bundle,1)
        for j=1:size(bundle{i,1},2)
            tmp = zeros(K, 1);
            for k=1:size(bundle{i,1}(1,j).feature_idx, 1)
                tmp(bundle{i,1}(1,j).feature_idx(k,1), 1) = tmp(bundle{i,1}(1,j).feature_idx(k,1), 1) + 1;
            end
            bundle{i,1}(1,j).ROF = tmp;
        end
    end
end

